#include <stdio.h>

int main() {
    int A, B, C;

    scanf("%d %d %d", &A, &B, &C);
    
    // 현재분에 요리시간을 더함
    B += C;
    
    // 분이 60이상이면 시로 올림처리
    if (B >= 60) {
        A += B / 60;  // 60분마다 1시간 추가
        B %= 60;      // 나머지 분만 남김
        
        // 시가 24를 초과하면 자정을 넘긴 것이므로 24로 나눈 나머지
        if (A > 24)
            A %= 24;
        // 시가 정확히 24이면 자정(0시)으로 처리
        else if (A == 24) {
            A = 0;
        }
    }
    
  
    printf("%d %d", A, B);
    
    return 0;
}
